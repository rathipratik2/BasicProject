package Basicjava;

public class Unary {

	public static void main(String[] args) {
		
		int a=14;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a=-a);
		System.out.println(--a);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(a--);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(a=+a);
		System.out.println(a=-a);
		System.out.println(a++);

	}

}
